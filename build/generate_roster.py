"""generate_roster.py — build window.MEMBER_ROSTER from member data files.

Reads dashboards/members/<bio>/data.js (required) and prudence.js (optional)
for every member directory, then writes dashboards/roster_summary.js.

Run after adding a new member:
  python3 build/generate_roster.py
"""

import json, re
from pathlib import Path

ROOT        = Path(__file__).resolve().parent.parent
MEMBERS_DIR = ROOT / "dashboards" / "members"
OUT         = ROOT / "dashboards" / "roster_summary.js"

SWALWELL_BIO = "S001193"

def fmt_total(n):
    if n >= 1_000_000:
        v = n / 1_000_000
        return f"${v:.1f}M"
    if n >= 1_000:
        return f"${n/1000:.0f}K"
    return f"${n:,.0f}"

def initials(first, last):
    return ((first[:1] if first else '') + (last[:1] if last else '')).upper()

def party_key(party_str):
    return 'D' if 'democrat' in party_str.lower() else 'R' if 'republican' in party_str.lower() else '?'

def parse_js(path):
    raw = path.read_text(encoding='utf-8')
    # strip window.XXXX = ... ;
    json_str = re.sub(r'^\s*window\.\w+\s*=\s*', '', raw).rstrip().rstrip(';')
    return json.loads(json_str)

def status_label(status_str, bio):
    s = status_str or ''
    if s.lower().startswith('sitting'):
        return 'Sitting member'
    if 'left during' in s.lower() or 'left' in s.lower():
        return 'Left 119th Congress'
    return 'Sitting member'

roster = []

def process_member(bio_dir, data_js_path, prudence_js_path=None):
    bio = bio_dir.name
    try:
        D = parse_js(data_js_path)
    except Exception as e:
        print(f"  SKIP {bio}: {e}")
        return None

    m = D.get("member", {})
    ents = D.get("entities", {})
    flags = D.get("flags", [])

    # Handle both snake_case (standard data.js) and Title Case (swalwell_data.js)
    first = m.get("first_name") or m.get("First Name", "")
    last  = m.get("last_name")  or m.get("Last Name",  "")
    full  = m.get("full_name")  or m.get("Full Name")  or f"{first} {last}".strip()
    state = m.get("state")      or m.get("State", "")
    district = str(m.get("district") or m.get("District", ""))
    party = party_key(m.get("party") or m.get("Party", ""))
    party_full = "Democrat" if party == "D" else "Republican"
    status = m.get("status") or m.get("Status / Bioguide", "")

    total = 0
    for ent in ents.values():
        if isinstance(ent, dict):
            total += ent.get("total", 0)

    flag_count = len(flags)
    high_flags = sum(1 for f in flags if f.get("severity", "").lower() == "high")

    pjs = prudence_js_path or (bio_dir / "prudence.js")
    prudence_count = 0
    if pjs.exists():
        try:
            P = parse_js(pjs)
            prudence_count = len(P.get("findings", []))
        except Exception:
            pass

    slug = "swalwell" if bio == SWALWELL_BIO else bio

    entry = {
        "slug": slug,
        "name": full,
        "initials": initials(first, last),
        "state": state,
        "district": district,
        "party": party,
        "partyFull": party_full,
        "congress": "119th",
        "statusLabel": status_label(status, bio),
        "totalUniverse": fmt_total(total),
        "flagCount": flag_count,
        "highFlags": high_flags,
        "prudenceFindings": prudence_count,
        "hasData": True,
    }
    print(f"  {bio}: {full} ({party}) flags={flag_count} prudence={prudence_count} total={fmt_total(total)}")
    return entry

for bio_dir in sorted(MEMBERS_DIR.iterdir()):
    data_js = bio_dir / "data.js"
    # Swalwell special case: data lives at dashboards/swalwell_data.js
    if bio_dir.name == SWALWELL_BIO:
        data_js = ROOT / "dashboards" / "swalwell_data.js"
    if not data_js.exists():
        continue

    entry = process_member(bio_dir, data_js)
    if entry:
        roster.append(entry)

# Write output
lines = [
    "/* AUTO-GENERATED by build/generate_roster.py — do not edit by hand */",
    "/* Run: python3 build/generate_roster.py  after adding a new member */",
    "",
    f"window.MEMBER_ROSTER = {json.dumps(roster, indent=2)};",
    "",
]
OUT.write_text("\n".join(lines), encoding="utf-8")
print(f"\nWritten {len(roster)} members → {OUT}")
